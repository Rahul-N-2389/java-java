package elc_blc;

public class Employeee {
	int empId;
	String empName;
	int empSalary;
	
	public int getEmpId() {
		return empId;
		
	}

	public String getEmpName() {
		return empName;
	}
	public int getEmpSalary() {
		return empSalary;
	}
	public void setEmpId(int id) {
		empId=id;
	}
	public void setEmpName(String Name) {
		empName=Name;
	}
	public void setEmpSalary(int Salary) {
		empSalary=Salary;
		
	}
}
