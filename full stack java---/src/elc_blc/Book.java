package elc_blc;

public class Book {
	int bookId;
	String title;
	String author;
	int price;
	
	public int getBookId() {
		return bookId;
	}
	public String getTitle() {
		return title;
	}
	public String getAuthor() {
		return author;
	}

	public int getPrice() {
	return price;
	}
	public void setBookId(int id) {
		bookId=id;
	}
	public void setTitle(String name) {
		title=name;
	
	}
	public void setAuthor(String writer) {
		author=writer;
	}

	public void setPrice(int cost) {
		price=cost;
}





}
