package elc_blc;

public class Employee {
	int employeeId;
	String employeeName;
	int salary;
	
	public int getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(int Id) {
		employeeId=Id;
	}
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String name) {
		employeeName=name;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int sal) {
		salary=sal;
	}

}
